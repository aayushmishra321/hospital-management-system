/* ======================================================
   ðŸ§  HELPER FUNCTIONS
   ====================================================== */

export const getLoginImage = (role: string) =>
  UNSPLASH_IMAGES.auth[role as keyof typeof UNSPLASH_IMAGES.auth] ||
  UNSPLASH_IMAGES.auth.register;

export const getDashboardImage = (role: string, page = 'main') =>
  UNSPLASH_IMAGES.dashboards[role as keyof typeof UNSPLASH_IMAGES.dashboards]?.[page] ||
  UNSPLASH_IMAGES.landing.hero;

export const getDepartmentImage = (dept: string) =>
  UNSPLASH_IMAGES.departments[dept as keyof typeof UNSPLASH_IMAGES.departments] ||
  UNSPLASH_IMAGES.dashboards.doctor.main;

export const getLegacyImage = (type: 'logo' | 'loader', role?: string) => {
  if (type === 'logo') {
    return role === 'patient'
      ? UNSPLASH_IMAGES.legacy.patientLogo
      : UNSPLASH_IMAGES.legacy.adminLogo;
  }
  return role === 'patient'
    ? UNSPLASH_IMAGES.legacy.patientLoader
    : UNSPLASH_IMAGES.legacy.adminLoader;
};

// ðŸ”„ BACKWARD COMPATIBILITY FUNCTIONS
// These ensure existing components continue to work with the new system

export const getRoleDashboardImage = (role: string): string => {
  return getDashboardImage(role, 'main');
};

export const getRoleLoginImage = (role: string): string => {
  return getLoginImage(role);
};

export const getSmartImage = (context: {
  role?: string;
  page?: string;
  department?: string;
  functionality?: string;
  isMobile?: boolean;
  isLogin?: boolean;
}): string => {
  const { role, page, department, functionality, isMobile, isLogin } = context;
  
  // Login context
  if (isLogin && role) {
    return getLoginImage(role);
  }
  
  // Department context
  if (department) {
    return getDepartmentImage(department);
  }
  
  // Role + Page context
  if (role && page) {
    return getDashboardImage(role, page);
  }
  
  // Role dashboard
  if (role) {
    return getDashboardImage(role, 'main');
  }
  
  // Default fallback
  return UNSPLASH_IMAGES.landing.hero;
};

